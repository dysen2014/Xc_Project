public class tAddr {

    String id;
    String mProName, mProSort, mProRemark;
    String mCityName, mCitySort;
    String mZoneName;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getmProName() {
        return mProName;
    }

    public void setmProName(String mProName) {
        this.mProName = mProName;
    }

    public String getmProSort() {
        return mProSort;
    }

    public void setmProSort(String mProSort) {
        this.mProSort = mProSort;
    }

    public String getmProRemark() {
        return mProRemark;
    }

    public void setmProRemark(String mProRemark) {
        this.mProRemark = mProRemark;
    }

    public String getmCityName() {
        return mCityName;
    }

    public void setmCityName(String mCityName) {
        this.mCityName = mCityName;
    }

    public String getmCitySort() {
        return mCitySort;
    }

    public void setmCitySort(String mCitySort) {
        this.mCitySort = mCitySort;
    }

    public String getmZoneName() {
        return mZoneName;
    }

    public void setmZoneName(String mZoneName) {
        this.mZoneName = mZoneName;
    }
}
